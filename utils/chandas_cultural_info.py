def get_chandas_info(metre_name: str) -> dict:
    """
    Get cultural and symbolic information for a given chandas
    
    Args:
        metre_name: Name of the metre (case-insensitive)
        
    Returns:
        Dictionary with cultural information or None if not found
    """
    if not metre_name:
        return None
    
    # Case-insensitive lookup
    for key, value in CHANDAS_CULTURAL_INFO.items():
        if key.lower() == metre_name.lower():
            return value
    
    return None


def format_chandas_context(metre_name: str) -> str:
    """
    Format chandas cultural information as a readable string
    
    Args:
        metre_name: Name of the metre
        
    Returns:
        Formatted string with cultural context
    """
    info = get_chandas_info(metre_name)
    
    if not info:
        return ""
    
    context = f"""
**{metre_name} Metre Cultural Context:**

**Structure:** {info['structure']} ({info['syllables']} syllables total)
**Symbolism:** {info['symbolism']}
**Associated Deity:** {info['deity']}
**Spiritual Meaning:** {info['meaning']}
**Significance:** {info['significance']}
"""
    
    return context.strip()
